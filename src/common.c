#include <stdlib.h>
#include <assert.h>

#include "common-private.h"

void
dc_status_set_error (dc_status_t *status, dc_status_t error)
{
	assert (status != NULL);

	if (*status == DC_STATUS_SUCCESS)
		*status = error;
}
